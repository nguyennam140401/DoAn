const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const roleValidation = require('../../validations/role.validation');
const roleController = require('../../controllers/role.controller');
const { permission } = require('../../config/permission');

const router = express.Router();

router
  .route('/')
  .post(auth(permission.ROLE.MANAGE_ROLE), validate(roleValidation.createrole), roleController.createRole)
  .get(auth(permission.ROLE.GET_ROLE), validate(roleValidation.getroles), roleController.getRoles);

router.get('/permission', auth(permission.ROLE.MANAGE_ROLE), roleController.getPermission);

router
  .route('/:roleId')
  .get(auth(permission.ROLE.GET_ROLE), validate(roleValidation.getrole), roleController.getRole)
  .patch(auth(permission.ROLE.MANAGE_ROLE), validate(roleValidation.updaterole), roleController.updateRole)
  .delete(auth(permission.ROLE.MANAGE_ROLE), validate(roleValidation.deleterole), roleController.deleteRole);

module.exports = router;

